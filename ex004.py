n1 = input('Escreva algo:')
print(type (n1))
print('é alfanumerico?', n1.isalnum())
print('é alfabetico?', n1.isalpha())

print('é decimal?', n1.isdecimal())
print('é em minusculas?', n1.islower())

print('é somente espaço?', n1.isspace())
print('esta capitalizada?', n1.istitle())
print('esta em maiusculos?', n1.isupper())

print('é numerico?', n1.isnumeric())
